    private void editbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editbtnActionPerformed
        // TODO add your handling code here:
        try{
            Connection con=DbConnect.connect();
            
            PreparedStatement ps=con.prepareStatement("update event set date=?, eventName=?,description=? where id=?");
            Date date=d1.getDate();
            SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
            String fd=sdf.format(date);
            ps.setString(1,fd);
            
            if(newName==null){
                ps.setString(2,c1.getSelectedItem().toString());
            }
            else{
                ps.setString(2,newName);
            }
            ps.setString(3,desc.getText());
            ps.setString(4,id);
            
            int a=ps.executeUpdate();
            if(a==1){
                JOptionPane.showMessageDialog(null,"Event updated successfully");
            }else{
                JOptionPane.showMessageDialog(null,"Something went wrong");
            }
            
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_editbtnActionPerformed

    private void dateselectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dateselectActionPerformed
        // TODO add your handling code here:
        try{
            Connection con=DbConnect.connect();
            PreparedStatement ps=con.prepareStatement("select * from event where date=?");
            Date date=d1.getDate();
            SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
            String fd=sdf.format(date);
            ps.setString(1,fd);
            ResultSet rs=ps.executeQuery();
            int c12=0;
            while(rs.next()){
                c12++;
                c1.addItem(rs.getString("eventName"));
                desc.setText(rs.getString("description"));
            }
            if(c12==0){
                JOptionPane.showMessageDialog(null,"No such event exist");
            }
            
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_dateselectActionPerformed

