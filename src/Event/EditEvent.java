    private void c1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_c1ActionPerformed
        // TODO add your handling code here:
        try{
            Connection con=DbConnect.connect();
            PreparedStatement ps=con.prepareStatement("select * from event where date=? and eventName=?");
            Date date=d1.getDate();
            SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
            String fd=sdf.format(date);
            ps.setString(1,fd);
            ps.setString(2,c1.getSelectedItem().toString());
            
            ResultSet rs=ps.executeQuery();
            
            if(rs.next()){
                id=rs.getString("id");
                desc.setText(rs.getString("description"));
            }else{
                JOptionPane.showMessageDialog(null,"No such event exist");
            }
            
        }catch(Exception e){
            System.out.println(e);
        }
        
    }//GEN-LAST:event_c1ActionPerformed

